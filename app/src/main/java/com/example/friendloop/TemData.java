package com.example.friendloop;

public class TemData {
    public String mName;
    public String mPhone;
    public String mBirthday;
    public String mPreferences;

    public TemData() {
    }
    public TemData(String name, String phone, String birthday, String preferences) {
        this.mName = name;
        this.mPhone = phone;
        this.mBirthday = birthday;
        this.mPreferences = preferences;
    }
}
